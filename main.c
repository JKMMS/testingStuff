#include <stdio.h>  //Biblioteca padrão de entrada e saida de dados
#include <stdlib.h> //Biblioteca padrão das outras coisas

int main (void){ //Funcção principal

  int x = 0, y = 0; //Declaração de duas variaveis inteiras chamadas x e y inicializadas em 0

  printf("Digite um numero: "); //comentário de bosta
  scanf("%i", &x); //comentário de bosta

  printf("Digite outro numero: "); //comentário de bosta
  scanf("%i", &y); //comentário de bosta

  printf("A soma de %i e %i resulta em %i.", x, y, x + y); //comentário de bosta

  return 0; //comentário de bosta

}
